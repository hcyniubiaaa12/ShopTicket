<script setup>
import { ref, computed, onMounted, watch } from 'vue'
import { useRoute, useRouter } from 'vue-router'
import { fetchAllType } from '../../public/util/type/fetchAllType'
import { fetchPerformanceById } from '../../public/util/performance/fetchPerformance'
import { useLoginStore } from '@/stores/login'
const route = useRoute()
const router = useRouter()
const loginStore = useLoginStore()
const activeFooterItem = ref('ticket')
const categories = ref([])

// Ê¥ªË∑ÉÂàÜÁ±ª
const activeCategory = ref(route.query.category || null)

// ÊºîÂá∫Êï∞ÊçÆ
const events = ref([])

// Â∫ïÈÉ®ÂØºËà™È°π
const footerItems = ref([
  { id: 'home', name: 'È¶ñÈ°µ', icon: 'üè†' },
  { id: 'ticket', name: 'ÊºîÂá∫', icon: '‚ù§Ô∏è' },
  { id: 'userticket', name: 'Á•®Â§π', icon: 'üéüÔ∏è' },
  { id: 'order', name: 'ËÆ¢Âçï', icon: 'üìã' },
  { id: 'profile', name: 'ÊàëÁöÑ', icon: 'üë§' }
])

// ========== ‰øÆÂ§çÊ†∏ÂøÉÔºöÂ∞Ü async ÈÄªËæëÁßªÂá∫ computed ==========

// Ê†πÊçÆÂàÜÁ±ªÁ≠õÈÄâÊºîÂá∫ÔºàÂêåÊ≠• computedÔºâ
const filteredEvents = computed(() => {

  // ÊâæÂà∞ÂΩìÂâçÂàÜÁ±ªÁöÑ nameÔºàÂ¶Ç "ÊºîÂî±‰ºö"Ôºâ
  const selectedCategory = categories.value.find(cat => cat.id == activeCategory.value)

  if (!selectedCategory) return []

  // ‰ΩøÁî® typeName ÂåπÈÖç

   
  return events.value.filter(event => event.typeName === selectedCategory.name)
})

// ËÆæÁΩÆÂàÜÁ±ªÂπ∂Âä†ËΩΩÂØπÂ∫îÊï∞ÊçÆ
const setActiveCategory = async (categoryId) => {
  activeCategory.value = categoryId
  try {
    let { data } = await fetchPerformanceById(categoryId)
    if (data && data.code === 200) {
      events.value = data.data
      console.log('Ëé∑ÂèñÊºîÂá∫Êï∞ÊçÆÊàêÂäü:', data.data);

    } else {
      events.value = []
      console.warn('Êï∞ÊçÆËé∑ÂèñÂ§±Ë¥•Êàñ‰∏∫Á©∫', data?.message)
    }
  } catch (err) {
    console.error('ËØ∑Ê±ÇÊºîÂá∫Êï∞ÊçÆÂ§±Ë¥•:', err)
    events.value = []
  }

  // Êõ¥Êñ∞ URL
  router.replace({
    query: { ...route.query, category: categoryId }
  })
}

// Ë∑≥ËΩ¨Âà∞ÊºîÂá∫ËØ¶ÊÉÖ
const goToEventDetail = (eventId,cityId) => {
  console.log('Êü•ÁúãÊºîÂá∫ËØ¶ÊÉÖ:', eventId);

  router.push({
    path: `/event/${eventId}`,
    query: { cityId: cityId }
  })
}

// ËÆæÁΩÆÂ∫ïÈÉ®ÂØºËà™
const setActiveFooterItem = (itemId) => {
  activeFooterItem.value = itemId
  switch (itemId) {
    case 'home':
      router.push('/')
      break
    case 'ticket':
      router.push('/ticket')
      break
    case 'userticket':
      router.push('/userticket')
      break
    case 'order':
      router.push('/order')
      break
    case 'profile':
      if (!loginStore.isLogin) {
        router.push('/login')
      } else {
        router.push('/profile')
      }
      break
  }
}

// È°µÈù¢ÂàùÂßãÂåñÔºöËé∑ÂèñÂàÜÁ±ªÂπ∂Âä†ËΩΩÈªòËÆ§Êï∞ÊçÆ
onMounted(async () => {
  try {
    const { data } = await fetchAllType()

    if (data.code === 200) {
      categories.value = data.data.map(item => ({
        id: item.id,
        name: item.name
      }))

      // ËÆæÁΩÆÈªòËÆ§ÂàÜÁ±ªÔºàURL ‰ºòÂÖàÔºåÂê¶ÂàôÈÄâÁ¨¨‰∏Ä‰∏™Ôºâ
      const initialCategory = route.query.category
        ? route.query.category
        : (categories.value.length > 0 ? categories.value[0].id : null)

      activeCategory.value = initialCategory
      await setActiveCategory(initialCategory) // Âä†ËΩΩÊï∞ÊçÆ
    } else {
      // Â§±Ë¥•Êó∂Âä†ËΩΩÂÖ®ÈÉ®ÊºîÂá∫
      await setActiveCategory(null)
    }
  } catch (err) {
    console.error('ÂàùÂßãÂåñÂàÜÁ±ªÂ§±Ë¥•:', err)
    await setActiveCategory(null)
  }
})

// ÁõëÂê¨ URL ‰∏≠ category ÂèÇÊï∞ÂèòÂåñ
watch(
  () => route.query.category,
  async (newId) => {
    if (newId !== activeCategory.value) {
      activeCategory.value = newId
      await setActiveCategory(newId)
    }
  }
)
</script>

